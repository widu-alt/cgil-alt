// { statements... }
//
// Emits a C block. The opening { is emitted by this visitor (not the caller).
// This is the "Allman style" brace placement.
// Called from: IfStmt, WhirlStmt, ForeStmt, and standalone block statements.
//
// NOTE: SpellDecl bodies are NOT routed through BlockStmt — they are iterated
// directly in visit(SpellDecl*) so the spell can implement the destined pattern.
void CodeGenVisitor::visit(BlockStmt* node) {
    if (currentPhase != Phase::IMPLEMENTATIONS) return;
    emitLine("{");
    indentLevel++;
    for (auto& stmt : node->statements) {
        stmt->accept(*this);
    }
    indentLevel--;
    emitLine("}");
}

// An expression used as a statement: process_data(data);
// Evaluates the expression for its side effects. Return value discarded.
void CodeGenVisitor::visit(ExprStmt* node) {
    if (currentPhase != Phase::IMPLEMENTATIONS) return;
    indent();
    node->expression->accept(*this);
    emit(";\n");
}

// Handles two cases:
//
// CASE 1 — STANCE TRANSITION: ctrl = Disk:Reading;
//   The right side is a stance reference (IdentifierExpr with stanceName set).
//   This is NOT a value assignment. It is a hardware state machine transition.
//   The __stance field in the C struct is updated to the discriminant value.
//
//   For pointer variables (own sigil*): ctrl->__stance = Disk_Reading;
//   For value variables (stack sigil):  my_disk.__stance = Disk_Idle;
//
//   FIXED: The original always emitted ->__stance even for stack variables.
//          Now checks stancePointerVars (populated from spell parameters).
//
// CASE 2 — PORTLINE WRITE: disk_status_port = target;
//   The left side is a portline variable. Cannot be assigned via C pointer.
//   Must emit: asm volatile("outb %0, %1" :: ...)
//
// CASE 3 — NORMAL ASSIGNMENT: sector_count = sector_count + 1;
//   Standard C assignment.
void CodeGenVisitor::visit(AssignStmt* node) {
    if (currentPhase != Phase::IMPLEMENTATIONS) return;

    // --- CASE 1: Stance Transition (right side is Disk:Reading etc.) ---
    auto* valIdent = dynamic_cast<IdentifierExpr*>(node->value.get());
    if (valIdent && !valIdent->stanceName.lexeme.empty()) {
        // Right side is a stance reference. Emit __stance update.
        indent();

        // Get the target variable name to check if it's a pointer.
        auto* targetIdent = dynamic_cast<IdentifierExpr*>(node->target.get());
        std::string stanceConstant = valIdent->token.lexeme + "_" + valIdent->stanceName.lexeme;

        if (targetIdent) {
            bool isPointer = stancePointerVars.count(targetIdent->token.lexeme) > 0;
            if (isPointer) {
                // Pointer access: ctrl->__stance = Disk_Reading;
                emit(targetIdent->token.lexeme + "->__stance = " + stanceConstant + ";\n");
            } else {
                // Value access: my_disk.__stance = Disk_Idle;
                emit(targetIdent->token.lexeme + ".__stance = " + stanceConstant + ";\n");
            }
        } else {
            // Complex target (member access etc.) — emit target expression then ->__stance.
            node->target->accept(*this);
            emit("->__stance = " + stanceConstant + ";\n");
        }
        return;
    }

    // --- CASE 2: Portline Write (left side is a portline variable) ---
    auto* targetIdent = dynamic_cast<IdentifierExpr*>(node->target.get());
    if (targetIdent && isPortline(targetIdent->token.lexeme)) {
        // Capture the value expression as a string by temporarily redirecting output.
        // This lets us pass the value into the outb/outw asm template.
        std::ostringstream valStream;
        std::ostream* prevOut = &out;
        // We can't easily redirect — instead build the value string via a helper.
        // For V1: emit the asm with the raw value expression inline.
        indent();
        std::string port = targetIdent->token.lexeme;
        std::string addr = portlineAddressMap.at(port);
        std::string type = portlineTypeMap.at(port);

        // Build value string by visiting the value node (this writes to out).
        // We emit the full asm directly to avoid the redirect problem.
        if (type == "rune") {
            emit("asm volatile(\"outb %0, %1\" :: \"a\"((uint8_t)(");
            node->value->accept(*this);
            emit(")), \"d\"((uint16_t)" + addr + "));\n");
        } else {
            emit("asm volatile(\"outw %0, %1\" :: \"a\"((uint16_t)(");
            node->value->accept(*this);
            emit(")), \"d\"((uint16_t)" + addr + "));\n");
        }
        return;
    }

    // --- CASE 3: Normal Assignment ---
    indent();
    node->target->accept(*this);
    emit(" = ");
    node->value->accept(*this);
    emit(";\n");
}

// yield (ctrl, data);    yield 0;    yield;
//
// TWO MODES depending on whether the enclosing spell has destined blocks:
//
// MODE A — Normal (no destined blocks): emit 'return <value>;'
//
// MODE B — Destined mode (inDestinedSpell = true):
//   Emit: __ret = <value>;
//         goto __destined_N;    (N = index of the FIRST destined label = LIFO entry)
//
//   This ensures the destined cleanup chain always fires before the function
//   actually returns, regardless of which yield path executes.
//
// TUPLE YIELD: For multi-value yields like yield (ctrl, data):
//   Emit a compound literal initializing the Tuple struct:
//   __ret = (Tuple_Disk_Omen_scroll_DiskError){ ctrl, data };
void CodeGenVisitor::visit(YieldStmt* node) {
    if (currentPhase != Phase::IMPLEMENTATIONS) return;

    if (node->values.empty()) {
        if (inDestinedSpell) emitLine("goto __destined_" + std::to_string(globalDestinedCounter - (int)currentDestinedBlocks.size()) + ";");
        else emitLine("return;");
        return;
    }

    // Helper to emit a value, automatically wrapping it in an Omen struct if necessary
    auto emitYieldValue = [&](Expr* val, bool isOmen) {
        if (isOmen) {
            bool isRuin = false;
            if (auto* call = dynamic_cast<CallExpr*>(val)) {
                if (auto* id = dynamic_cast<IdentifierExpr*>(call->callee.get())) {
                    if (id->token.lexeme == "ruin") isRuin = true;
                }
            }
            if (isRuin) {
                val->accept(*this); // already emits { .__is_ruin = 1 ... }
            } else {
                emit("{ .__is_ruin = 0, .__value = ");
                val->accept(*this);
                emit(" }");
            }
        } else {
            val->accept(*this);
        }
    };

    indent();
    if (inDestinedSpell) emit("__ret = ");
    else emit("return ");

    if (node->values.size() == 1) {
        emitYieldValue(node->values[0].get(), currentSpell && currentSpell->hasOmen);
    } else {
        emit("(" + currentReturnTypeName + "){ ");
        for (size_t i = 0; i < node->values.size(); ++i) {
            bool isOmen = (currentSpell && currentSpell->hasOmen && i == node->values.size() - 1);
            emitYieldValue(node->values[i].get(), isOmen);
            if (i < node->values.size() - 1) emit(", ");
        }
        emit(" }");
    }
    emit(";\n");

    if (inDestinedSpell) {
        int lastDeclared = (globalDestinedCounter - (int)currentDestinedBlocks.size()) + (int)currentDestinedBlocks.size() - 1;
        emitLine("goto __destined_" + std::to_string(lastDeclared) + ";");
    }
}

