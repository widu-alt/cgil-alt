// grimoire <hardware_defs.h>;    pact <stdlib.h>;
//
// FIXED: Now emits angle-bracket includes (#include <path.h>) not quoted ones.
// FIXED: Only emits during the pre-pass (when currentPhase == TYPES and caller
//        is the explicit grimoire pre-loop in generate()). Other phases: skip.
//
// Cgil grimoire and pact both use angle-bracket includes because they reference
// system or build-tree headers, not local relative paths.
void CodeGenVisitor::visit(GrimoireDecl* node) {
    if (currentPhase != Phase::TYPES) return;
    // Emit as a system include with angle brackets.
    // The path token contains just the header name (e.g., "hardware_defs").
    // We add .h because the parser stores the base name.
    emitLine("#include <" + node->path.lexeme + ">");
}

// rank DiskError { Timeout, HardwareFault, InvalidSector }
//
// Emits:
//   typedef uint16_t DiskError;
//   #define DiskError_Timeout       ((uint16_t)0)
//   #define DiskError_HardwareFault ((uint16_t)1)
//   #define DiskError_InvalidSector ((uint16_t)2)
//
// Variants are assigned sequential discriminants in declaration order.
// The typedef is uint16_t because that's what the Omen struct's __ruin field
// uses, and what all divine branch comparisons operate on.
void CodeGenVisitor::visit(RankDecl* node) {
    if (currentPhase != Phase::TYPES) return;

    emitLine("// rank " + node->name.lexeme);
    emitLine("typedef uint16_t " + node->name.lexeme + ";");
    for (size_t i = 0; i < node->variants.size(); ++i) {
        emitLine("#define " + node->name.lexeme + "_" + node->variants[i].lexeme +
                 " ((" + node->name.lexeme + ")" + std::to_string(i) + ")");
    }
    emitLine("");
}

// sigil Disk { stance Idle; stance Reading; stance Fault; soul16 sector_count; }
//
// Emits:
//   // Stance discriminants for Disk
//   #define Disk_Idle    ((uint16_t)0)
//   #define Disk_Reading ((uint16_t)1)
//   #define Disk_Fault   ((uint16_t)2)
//
//   typedef struct {
//       uint16_t __stance;       // implicit — always first field
//       uint16_t sector_count;
//   } Disk;
//
// FIXED: The original never emitted stance #defines. AssignStmt stance
// transitions emit "Disk_Reading" etc., so these defines are required.
// Without them every stance transition produced an undeclared identifier error.
//
// Also stores stance info in stanceMap for AssignStmt to use.
void CodeGenVisitor::visit(SigilDecl* node) {
    if (currentPhase != Phase::TYPES) return;

    // Emit stance discriminant #defines and populate stanceMap.
    if (!node->stances.empty()) {
        emitLine("// Stance discriminants for sigil " + node->name.lexeme);
        std::vector<std::string> stanceNames;
        for (size_t i = 0; i < node->stances.size(); ++i) {
            std::string defineStr = "#define " + node->name.lexeme + "_" +
                                    node->stances[i].lexeme +
                                    " ((uint16_t)" + std::to_string(i) + ")";
            emitLine(defineStr);
            stanceNames.push_back(node->stances[i].lexeme);
        }
        stanceMap[node->name.lexeme] = stanceNames;
        emitLine("");
    }

    // Emit the struct typedef.
    emitLine("// sigil " + node->name.lexeme);
    emitLine("typedef struct {");
    indentLevel++;

    // The implicit __stance field is ALWAYS first per spec.
    // This guarantees predictable memory layout — other code can cast to
    // a generic "has __stance as first field" type.
    if (!node->stances.empty()) {
        emitLine("uint16_t __stance; // implicit stance discriminant — do not access directly");
    }

    // User-declared fields follow in declaration order.
    for (const auto& field : node->fields) {
        emitLine(getCType(field.type) + " " + field.name.lexeme + ";");
    }

    indentLevel--;
    emitLine("} " + node->name.lexeme + ";");
    emitLine("");

    // Emit Omen/Tuple typedefs for any bound spells.
    // This handles methods declared inside a sigil body.
    for (auto& boundSpell : node->boundSpells) {
        if (boundSpell->hasOmen && !boundSpell->returnTypes.empty()) {
            std::string successCType = getCType(boundSpell->returnTypes[0].typeToken);
            emitOmenTypedefIfNeeded(
                successCType,
                boundSpell->returnTypes[0].typeToken.lexeme,
                boundSpell->omenErrorType.lexeme
            );
        }
        if (boundSpell->returnTypes.size() > 1 || boundSpell->hasOmen) {
            emitTupleTypedefIfNeeded(boundSpell.get());
        }
    }
}

// legion SectorCache { mark16 sector_id; flow read_time; oath is_corrupted; }
//
// V1 STUB: Emitted as a standard C struct (no SoA transformation).
// The keyword 'legion' is tracked separately from 'sigil' so V2 can identify
// these nodes and apply the Structure of Arrays layout transformation.
void CodeGenVisitor::visit(LegionDecl* node) {
    if (currentPhase != Phase::TYPES) return;

    emitLine("// legion " + node->name.lexeme + " (V1 stub: emitted as struct)");
    emitLine("typedef struct {");
    indentLevel++;
    for (const auto& field : node->fields) {
        emitLine(getCType(field.type) + " " + field.name.lexeme + ";");
    }
    indentLevel--;
    emitLine("} " + node->name.lexeme + ";");
    emitLine("");
}

// leyline disk_status_port: rune @ 0x1F7;
// portline disk_data_port: soul16 @ 0x1F0;
//
// leyline (MMIO):
//   Emits a global volatile pointer locked to the physical address.
//   volatile uint8_t* const disk_status_port = (volatile uint8_t*)0x1F7;
//   The 'volatile' prevents the optimizer from caching the value.
//   'const' prevents the pointer itself from being reassigned.
//
// portline (PIO):
//   Does NOT emit a C variable — x86 Port I/O is a separate address space
//   accessed only via 'in'/'out' CPU instructions. There is no C pointer that
//   maps to port I/O. Instead:
//   - Stores the port address in portlineAddressMap for use at read/write sites.
//   - Reads are intercepted in visit(IdentifierExpr*) -> emitPortlineRead().
//   - Writes are intercepted in visit(AssignStmt*) -> emitPortlineWrite().
//   - &portline_name is intercepted in visit(AddressOfExpr*) to emit the address.
//
// FIXED: The original emitted portlines in Phase::IMPLEMENTATIONS. Moved to TYPES
//        so portline info is populated before PROTOTYPES and IMPLEMENTATIONS need it.
void CodeGenVisitor::visit(HardwareDecl* node) {
    if (currentPhase != Phase::TYPES) return;

    if (!node->isPortline) {
        // leyline (MMIO): emit volatile pointer global.
        // This MUST appear before any function that references it.
        // By emitting in TYPES phase, it precedes all prototypes and bodies.
        std::string cType = getCType(node->type);
        emitLine("// leyline " + node->name.lexeme + " @ " + node->address.lexeme);
        emitLine("volatile " + cType + "* const " + node->name.lexeme +
                 " = (volatile " + cType + "*)" + node->address.lexeme + ";");
        emitLine("");
    } else {
        // portline (PIO): store address and type, emit comment.
        // The actual inb/inw/outb/outw asm is emitted at usage sites.
        portlineAddressMap[node->name.lexeme]  = node->address.lexeme;
        portlineTypeMap[node->name.lexeme]     = node->type.lexeme;
        emitLine("// portline " + node->name.lexeme + " @ " +
                 node->address.lexeme + " (reads/writes emit inline asm)");
        emitLine("");
    }
}

// Helper: emit a portline READ as a GCC statement expression.
// Wraps the asm in ({ ... }) so it can appear inside any C expression.
//
// rune   (8-bit):
//   ({ uint8_t __tmp; asm volatile("inb %1, %0" : "=a"(__tmp) : "d"((uint16_t)PORT)); __tmp; })
//
// soul16 (16-bit):
//   ({ uint16_t __tmp; asm volatile("inw %1, %0" : "=a"(__tmp) : "d"((uint16_t)PORT)); __tmp; })
void CodeGenVisitor::emitPortlineRead(const std::string& name) {
    std::string addr = portlineAddressMap.at(name);
    std::string type = portlineTypeMap.at(name);

    if (type == "rune") {
        emit("({ uint8_t __tmp; "
             "asm volatile(\"inb %1, %0\" : \"=a\"(__tmp) : \"d\"((uint16_t)" +
             addr + ")); __tmp; })");
    } else {
        // soul16 or addr
        emit("({ uint16_t __tmp; "
             "asm volatile(\"inw %1, %0\" : \"=a\"(__tmp) : \"d\"((uint16_t)" +
             addr + ")); __tmp; })");
    }
}

// Helper: emit a portline WRITE as inline asm.
// The value expression is passed as a pre-built string (already emitted by caller).
//
// rune   (8-bit):  asm volatile("outb %0, %1" :: "a"((uint8_t)(VAL)),  "d"((uint16_t)PORT));
// soul16 (16-bit): asm volatile("outw %0, %1" :: "a"((uint16_t)(VAL)), "d"((uint16_t)PORT));
void CodeGenVisitor::emitPortlineWrite(const std::string& name,
                                        const std::string& valExpr) {
    std::string addr = portlineAddressMap.at(name);
    std::string type = portlineTypeMap.at(name);

    if (type == "rune") {
        emit("asm volatile(\"outb %0, %1\" :: \"a\"((uint8_t)(" + valExpr + ")), "
             "\"d\"((uint16_t)" + addr + "))");
    } else {
        emit("asm volatile(\"outw %0, %1\" :: \"a\"((uint16_t)(" + valExpr + ")), "
             "\"d\"((uint16_t)" + addr + "))");
    }
}

// Helper: emit an Omen typedef if it hasn't been emitted yet.
//
// An Omen is a tagged union representing either a success value or a typed error:
//   typedef struct {
//       uint8_t __is_ruin;     // 0 = success, 1 = ruin
//       union {
//           Cgil_Scroll __value;  // success payload (type varies)
//           uint16_t    __ruin;   // error discriminant (from rank)
//       };
//   } Omen_scroll_DiskError;
//
// The naming convention: Omen_<successTypeName>_<ruinRankName>
// This must be emitted exactly once per unique combination.
void CodeGenVisitor::emitOmenTypedefIfNeeded(const std::string& successCType,
                                              const std::string& successTypeName,
                                              const std::string& ruinTypeName) {
    if (ruinTypeName.empty()) return; // No Omen if no ruin type
    std::string omenName = getOmenTypeName(successTypeName, ruinTypeName);

    if (emittedOmenTypes.count(omenName)) return; // Already emitted
    emittedOmenTypes.insert(omenName);

    emitLine("// Omen type: " + successTypeName + " | ruin<" + ruinTypeName + ">");
    emitLine("typedef struct {");
    indentLevel++;
    emitLine("uint8_t __is_ruin; // 0 = success, 1 = ruin");
    emitLine("union {");
    indentLevel++;

    // LANDMINE 1 FIX:
    //   If the success type is 'abyss' (void), there is no success payload.
    //   A C union cannot contain a void member — 'void __value;' is a fatal
    //   compile error. When the spell returns 'abyss | ruin<T>', we omit
    //   the __value field entirely. The __is_ruin flag is still meaningful:
    //   0 = success (no payload), 1 = ruin (discriminant in __ruin).
    if (successCType != "void") {
        emitLine(successCType + " __value; // success payload");
    } else {
        emitLine("// no __value: success type is abyss (void), no payload stored");
    }

    emitLine("uint16_t __ruin;          // ruin discriminant (rank variant value)");
    indentLevel--;
    emitLine("};");
    indentLevel--;
    emitLine("} " + omenName + ";");
    emitLine("");
}

// Helper: compute a Tuple typedef name from a spell's return type.
// (sigil* Disk, scroll | ruin<DiskError>) -> "Tuple_Disk_Omen_scroll_DiskError"
//
// For V1 we use a simplified naming that covers the common case of
// (sigil*, omen) tuples. Full generic naming would require deeper type analysis.
std::string CodeGenVisitor::getTupleTypeName(const SpellDecl* node) const {
    if (node->returnTypes.empty()) return "Tuple_Empty";

    std::string name = "Tuple";
    for (const auto& rt : node->returnTypes) {
        name += "_" + rt.typeToken.lexeme;
        if (rt.isPointer) name += "Ptr";
    }
    if (node->hasOmen && !node->omenErrorType.lexeme.empty()) {
        name += "_Omen_" + node->omenErrorType.lexeme;
    }
    return name;
}

// Helper: emit a Tuple struct typedef if it hasn't been emitted yet.
//
// Tuples are used as multi-value return types:
//   (sigil* Disk, scroll | ruin<DiskError>) emits:
//   typedef struct {
//       Disk* __elem0;
//       Omen_scroll_DiskError __elem1;
//   } Tuple_Disk_Omen_scroll_DiskError;
//
// The <~ rebinding in divine extracts __elem0.
// The pattern matching in divine inspects __elem1.
void CodeGenVisitor::emitTupleTypedefIfNeeded(SpellDecl* node) {
    if (node->returnTypes.size() <= 1 && !node->hasOmen) return;

    std::string tupleName = getTupleTypeName(node);
    if (emittedTupleTypes.count(tupleName)) return;
    emittedTupleTypes.insert(tupleName);

    emitLine("// Tuple return type for spell " + node->name.lexeme);
    emitLine("typedef struct {");
    indentLevel++;

    // __elem0: First return type (the ownership pointer for <~ rebinding).
    // LANDMINE 2 FIX:
    //   The original guessed isPointer from tuple size — completely wrong for
    //   tuples like (mark16, soul16) where neither element is a pointer.
    //   ReturnTypeInfo.isPointer is now set by the Parser when '*' was present.
    //   We read it here exactly. No guessing.
    if (!node->returnTypes.empty()) {
        std::string elem0Type = getCType(node->returnTypes[0].typeToken);
        if (node->returnTypes[0].isPointer) {
            emitLine(elem0Type + "* __elem0; // ownership pointer (<~ rebinding target)");
        } else {
            emitLine(elem0Type + " __elem0;  // first return element");
        }
    }

    // __elem1: The Omen payload (success or ruin), if present.
    if (node->hasOmen && !node->omenErrorType.lexeme.empty() &&
        node->returnTypes.size() > 1) {
        std::string successTypeName = node->returnTypes[1].typeToken.lexeme;
        std::string omenName = getOmenTypeName(successTypeName, node->omenErrorType.lexeme);
        emitLine(omenName + " __elem1; // omen payload (success or ruin)");
    }

    indentLevel--;
    emitLine("} " + tupleName + ";");
    emitLine("");
}

// Helper: scan the top-level body of a spell for DestinedStmt nodes.
// Only looks at the direct children of the spell body — does NOT recurse into
// nested blocks. This matches the spec: destined blocks are always declared at
// the top level of a spell body.
std::vector<DestinedStmt*> CodeGenVisitor::findDestinedBlocks(
    const std::vector<std::unique_ptr<Stmt>>& body) const
{
    std::vector<DestinedStmt*> result;
    for (const auto& stmt : body) {
        if (auto* d = dynamic_cast<DestinedStmt*>(stmt.get())) {
            result.push_back(d);
        }
    }
    return result;
}

// spell fetch_sector(...)   warden spell disk_irq()   conjure spell read_buffer(...)
//
// Emits:
//   PROTOTYPES phase: The function signature ending in ';'
//   IMPLEMENTATIONS phase: The function signature + body
//
// DESTINED BLOCK EXPANSION:
//   If the spell contains destined blocks, we use a two-pass approach:
//   1. Scan body for destined blocks, collect them into currentDestinedBlocks.
//   2. Declare a '__ret' local variable of the return type.
//   3. Walk the body. visit(YieldStmt*) checks inDestinedSpell and emits:
//        __ret = <value>; goto __destined_N;
//      instead of: return <value>;
//   4. After the body, emit destined labels in LIFO order (last declared fires first).
//   5. Final 'return __ret;' at the very end.
//
// WARDEN SPELL:
//   'warden' emits '__attribute__((interrupt))' before the function signature.
//   This tells GCC to use the interrupt calling convention (different epilogue
//   that correctly restores the x86 interrupt stack frame).
//
// CONJURE SPELL:
//   External C function declaration. No body is emitted.
//   'conjure endless' marks noreturn: '__attribute__((noreturn))'.
//
// FIXED: Added returnTypes.empty() check to prevent out-of-bounds crash.
// FIXED: Omen and Tuple typedefs are now emitted in TYPES phase.
void CodeGenVisitor::visit(SpellDecl* node) {
    currentSpell = node;
    // --- TYPES PHASE: Emit Omen and Tuple typedefs for this spell's return type ---
    if (currentPhase == Phase::TYPES) {
        if (node->hasOmen && !node->returnTypes.empty()) {
            // Find the success type. For tuples, it's the second element.
            // For bare omens (scroll | ruin<E>), it's the first element.
            size_t successIdx = (node->returnTypes.size() > 1) ? 1 : 0;
            if (successIdx < node->returnTypes.size()) {
                std::string successCType    = getCType(node->returnTypes[successIdx].typeToken);
                std::string successTypeName = node->returnTypes[successIdx].typeToken.lexeme;
                emitOmenTypedefIfNeeded(successCType, successTypeName, node->omenErrorType.lexeme);
            }
        }
        if (node->returnTypes.size() > 1) {
            emitTupleTypedefIfNeeded(node);
        }
        return;
    }

    // --- PROTOTYPES and IMPLEMENTATIONS: Build the C function signature ---

    // Determine the C return type string.
    // For tuple-returning spells (multiple return types with optional omen):
    //   Emit the Tuple struct name.
    // For omen-only returns (single value with ruin):
    //   Emit the Omen struct name.
    // For single primitive returns:
    //   Emit the C type directly.
    // FIXED: Check returnTypes.empty() before accessing [0].
    std::string returnTypeStr;
    if (node->returnTypes.size() > 1) {
        returnTypeStr = getTupleTypeName(node);
    } else if (node->hasOmen && !node->returnTypes.empty() &&
               !node->omenErrorType.lexeme.empty()) {
        returnTypeStr = getOmenTypeName(node->returnTypes[0].typeToken.lexeme,
                                        node->omenErrorType.lexeme);
    } else if (node->returnTypes.empty() ||
               node->returnTypes[0].typeToken.lexeme == "abyss") {
        returnTypeStr = "void";
    } else {
        returnTypeStr = getCType(node->returnTypes[0].typeToken);
    }

    // Build the full signature string.
    std::string signature;

    if (node->isConjure && node->isEndless) {
        signature += "__attribute__((noreturn)) ";
    }
    if (node->isWarden && currentPhase == Phase::IMPLEMENTATIONS) {
        signature += "__attribute__((interrupt)) ";
    }

    signature += returnTypeStr + " " + node->name.lexeme + "(";

    for (size_t i = 0; i < node->params.size(); ++i) {
        const auto& p = node->params[i];
        signature += getCType(p.type);
        if (p.isPointer) signature += "*";
        signature += " " + p.name.lexeme;
        if (i < node->params.size() - 1) signature += ", ";
    }
    signature += ")";

    if (currentPhase == Phase::PROTOTYPES) {
        emitLine(signature + ";");
        return;
    }

    // --- IMPLEMENTATIONS PHASE: Emit body ---

    // conjure spells are extern declarations with no body.
    if (node->isConjure) {
        emitLine("// extern: " + node->name.lexeme + " (declared via conjure)");
        return;
    }

    // Store return type for YieldStmt to use.
    currentReturnTypeName = returnTypeStr;

    // Populate stancePointerVars: track which parameters are sigil pointers.
    // This is used by AssignStmt to decide -> vs . for __stance access.
    stancePointerVars.clear();
    for (const auto& p : node->params) {
        if (p.isPointer) {
            stancePointerVars.insert(p.name.lexeme);
        }
    }

    // Two-pass destined block handling.
    // Pass 1: Find all destined blocks in this spell body.
    currentDestinedBlocks = findDestinedBlocks(node->body);
    inDestinedSpell = !currentDestinedBlocks.empty();

    emitLine(signature + " {");
    indentLevel++;

    // If there are destined blocks, declare the unified return slot __ret.
    // Every yield will assign to __ret and jump to the destined chain.
    if (inDestinedSpell && returnTypeStr != "void") {
        emitLine(returnTypeStr + " __ret; // unified return slot for destined RAII");
        emitLine("");
    }

    // Pass 2: Walk the body. Destined blocks themselves are skipped inline
    // (they will be emitted AFTER the body as labels, not where they appear).
    for (auto& stmt : node->body) {
        // Skip destined blocks during the main body pass.
        // They are emitted at the END of the function.
        if (dynamic_cast<DestinedStmt*>(stmt.get())) continue;
        stmt->accept(*this);
    }

    // Emit destined cleanup labels in LIFO order (last declared fires first).
    // LIFO: emit the last element first, chain falls through to earlier labels.
    if (inDestinedSpell) {
        emitLine("");
        emitLine("// --- Destined RAII Cleanup Chain (LIFO order) ---");

        // Generate a unique base index for this spell's labels.
        int baseIdx = globalDestinedCounter;
        globalDestinedCounter += (int)currentDestinedBlocks.size();

        // LIFO: iterate in reverse (last declared = highest index = emitted first).
        for (int i = (int)currentDestinedBlocks.size() - 1; i >= 0; --i) {
            DestinedStmt* d = currentDestinedBlocks[i];
            int labelIdx = baseIdx + i;

            // The label that yields jump to.
            // Label falls through to the next (lower-numbered) label automatically.
            emitLine("__destined_" + std::to_string(labelIdx) + ":;");

            if (d->hasCondition) {
                indent(); emit("if (");
                d->condition->accept(*this);
                emit(") {\n");
                indentLevel++;
                for (auto& s : d->body->statements) s->accept(*this);
                indentLevel--;
                emitLine("}");
            } else {
                for (auto& s : d->body->statements) s->accept(*this);
            }
        }

        // The final return of the unified slot.
        if (returnTypeStr != "void") {
            emitLine("return __ret;");
        } else {
            emitLine("return;");
        }
    }

    // Clean up spell state.
    inDestinedSpell = false;
    currentDestinedBlocks.clear();
    stancePointerVars.clear();

    indentLevel--;
    emitLine("}");
    emitLine("");
}

