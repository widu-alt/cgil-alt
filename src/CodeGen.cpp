#include "CodeGen.h"
#include <sstream>

// =============================================================================
// MAIN ENTRY POINT
// =============================================================================

void CodeGenVisitor::generate(ProgramNode* program) {

    // -------------------------------------------------------------------------
    // PRE-PASS: INCLUDES
    // Grimoire includes must appear FIRST in the C file — before all type
    // definitions — because headers may define types that our structs depend on.
    //
    // FIXED: The original emitted includes during Phase::TYPES, mixed in with
    // struct definitions. This breaks if a struct depends on a type from a header.
    // -------------------------------------------------------------------------
    emitLine("// ============================================================");
    emitLine("// GENERATED BY CGIL COMPILER v1.0");
    emitLine("// DO NOT EDIT BY HAND");
    emitLine("// ============================================================");
    emitLine("");
    emitLine("// --- System Includes ---");
    emitLine("#include <stdint.h>");
    emitLine("#include <stdbool.h>");
    emitLine("");

    // Emit all grimoire/pact includes now, before anything else.
    emitLine("// --- Cgil Grimoire Includes ---");
    currentPhase = Phase::TYPES; // Temporarily use TYPES so GrimoireDecl emits
    for (auto& decl : program->declarations) {
        if (auto* g = dynamic_cast<GrimoireDecl*>(decl.get())) {
            g->accept(*this);
        }
    }
    emitLine("");

    // -------------------------------------------------------------------------
    // PREAMBLE: CGIL RUNTIME STRUCTURES
    // These are always emitted, regardless of what the .gil source contains.
    // They are the fundamental C types that Cgil's type system builds on.
    // -------------------------------------------------------------------------
    emitLine("// --- Cgil Runtime Structures ---");
    emitLine("// scroll: a fat pointer (address + length). Allocates no heap.");
    emitLine("typedef struct { const uint8_t* ptr; uint16_t len; } Cgil_Scroll;");
    emitLine("");

    // -------------------------------------------------------------------------
    // PHASE 1: TYPES
    // Emit rank typedefs, sigil structs, legion structs, Omen typedefs,
    // and Tuple typedefs. Everything needed to declare function signatures.
    // -------------------------------------------------------------------------
    emitLine("// ============================================================");
    emitLine("// PHASE 1: TYPE DEFINITIONS");
    emitLine("// ============================================================");
    currentPhase = Phase::TYPES;
    for (auto& decl : program->declarations) {
        decl->accept(*this);
    }
    emitLine("");

    // -------------------------------------------------------------------------
    // PHASE 2: PROTOTYPES
    // Forward-declare every spell so any spell can call any other spell
    // regardless of declaration order in the .gil source.
    // -------------------------------------------------------------------------
    emitLine("// ============================================================");
    emitLine("// PHASE 2: FUNCTION PROTOTYPES");
    emitLine("// ============================================================");
    currentPhase = Phase::PROTOTYPES;
    for (auto& decl : program->declarations) {
        decl->accept(*this);
    }
    emitLine("");

    // -------------------------------------------------------------------------
    // PHASE 3: IMPLEMENTATIONS
    // Emit leyline global variables and spell bodies.
    // -------------------------------------------------------------------------
    emitLine("// ============================================================");
    emitLine("// PHASE 3: IMPLEMENTATIONS");
    emitLine("// ============================================================");
    currentPhase = Phase::IMPLEMENTATIONS;
    for (auto& decl : program->declarations) {
        decl->accept(*this);
    }
}

