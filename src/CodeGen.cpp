// if (condition) { } elif (condition) { } else { }
//
// Emits:
//   if (condition) {
//       ...
//   } else if (condition) {
//       ...
//   } else {
//       ...
//   }
void CodeGenVisitor::visit(IfStmt* node) {
    if (currentPhase != Phase::IMPLEMENTATIONS) return;

    indent(); emit("if (");
    node->condition->accept(*this);
    emit(") {\n");
    indentLevel++;
    for (auto& s : node->thenBranch->statements) s->accept(*this);
    indentLevel--;
    emitLine("}");

    for (auto& elif : node->elifBranches) {
        indent(); emit("else if (");
        elif.condition->accept(*this);
        emit(") {\n");
        indentLevel++;
        for (auto& s : elif.body->statements) s->accept(*this);
        indentLevel--;
        emitLine("}");
    }

    if (node->elseBranch) {
        emitLine("else {");
        indentLevel++;
        for (auto& s : node->elseBranch->statements) s->accept(*this);
        indentLevel--;
        emitLine("}");
    }
}

// fore (mark16 i = 0; i < 10; i++) { }
// -> for (int16_t i = 0; i < 10; i++) { }
void CodeGenVisitor::visit(ForeStmt* node) {
    if (currentPhase != Phase::IMPLEMENTATIONS) return;

    indent();
    emit("for (" + getCType(node->initType) + " " + node->initVar.lexeme + " = ");
    node->initValue->accept(*this);
    emit("; ");
    node->condition->accept(*this);
    emit("; ");
    node->increment->accept(*this);
    emit(") {\n");
    indentLevel++;
    for (auto& s : node->body->statements) s->accept(*this);
    indentLevel--;
    emitLine("}");
}

